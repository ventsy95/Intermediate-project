
public class ImputMismatchException extends Exception{

	private static final long serialVersionUID = 4700754780077805979L;

	public ImputMismatchException() {
		super();
	}

	public ImputMismatchException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImputMismatchException(String message) {
		super(message);
	}

	public ImputMismatchException(Throwable cause) {
		super(cause);
	}
	

}
